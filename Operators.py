# Operator in python

# Arithematic Operator
print("Arithmatic Operator :- ")
print("10 + 6 is " , 10+6)
print("10 - 6 is " , 10-6)
print("10 * 6 is " , 10*6)
print("10 ** 6 is " , 10**6)
print("10 % 6 is " , 10%6)
print("10 / 6 is " , 10/6)
print("10 // 6 is " , 10//6)

# Assigment Operator
print("Assignment Operator :- ")
x = 5
print(x)
x +=5     # ('/=' , '%=')
print(x)

# Comparission Operator
print("Comparission Operator :- ")
i = 5
print(i == 5)  # ( '!=' , '>=' , '<=' )

# Logical Operator
print("Logical Operator :- ")
a = True
b = False
print(a and a)
print(a and b)
print(b and a)
print(b and b)
print(a or b)

# Identity Operator
print("Identity Operator :- ")
a = True
b = False
print(a is b)
print(a is not b)

# Membership Operator
print("Membership Operator :- ")
list = [1,3,56,67,23,45,89,100]
print(67 in list)
print(1 not in list)

# Bitwise Operator
print("Bitwise Operator :- ")
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11

print(0 & 2)
print(0 | 3)